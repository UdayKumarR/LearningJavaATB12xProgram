class exam1_primitiveDataTypes {

    static char a;
    static int b;
    static float f;
    static double d;
    static byte e;
    static short c;
    static long l;
    static boolean bl;
    public static void main(String[] args) {


        System.out.println("default value of char a is "+ a);
        System.out.println("default value of int a is "+ b);
        System.out.println("default value of float f is "+ f);
        System.out.println("default value of double d is "+ d);
        System.out.println("default value of byte e is "+ e);
        System.out.println("default value of short c is "+ c);
        System.out.println("default value of long l is "+ l);
        System.out.println("default value of boolean bl is "+ bl);

    }


}
